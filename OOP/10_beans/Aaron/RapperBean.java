public class RapperBean{
	private int tattooAmount;
	private String style;
	private String name;

	public int getTattooAmount() {
		return tattooAmount;
	}
	public void setTattooAmount(int amount) {
		tattooAmount = amount;
	}
	public int getStyle() {
		return tattooAmount;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public String getName() {
		return name;
	}
	public void setName(String alias) {
		name = alias;
	}   

}